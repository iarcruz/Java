public class Continhas{
    public static void main(String args[]){
        int a,b,c;
    
        a = 19;
        b = 3;
        c = a + b;
        System.out.println("Valor da soma = " + c);

        c = a - b;
        System.out.println("Valor da subtração c = " + c);

        c = a * b;
        System.out.println("Valor da multiplicação c = " + c);

        c = a / b;
        System.out.println("Valor da divisão c = " + c);

        c = a % b;
        System.out.println("Valor do resto c = " + c);

        float d,e;

        d = (float)2.0;
        e = 5.0f;
    
        System.out.println("Valor do d = " + d);
        System.out.println("Valor do e = " + e);
    

    }
}